function filterBrand(){
    const selectedBrand = document.getElementById('filter').value;
    showBrands(selectedBrand);
}

function showBrands(selectedBrand){
    var filteredBrands = productItems.filter(function(product){
        return selectedBrand == "" || product.brandName == selectedBrand;
    });

    var product = document.getElementById("product");
    product.innerHTML = "";
    for(let x=0; x<filteredBrands.length; x++) {
        product.innerHTML += '<div class="items">' +
            '<div class="product-img">' +
            '<img src='+filteredBrands[x].imgSrc+'>' +
            '</div>' +
            '<h6 class="brandname">'+filteredBrands[x].brandName+'</h6>' +
            '<span class="description">'+filteredBrands[x].description+'</span>' +
            '<span class="price">'+filteredBrands[x].price+'</span>' +
            '<button onclick="addToCart('+x+')" id="cart-btn">Add to Cart</button>' +
        '</div>';
    }
}
